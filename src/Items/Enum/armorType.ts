/**
 * Enum for armor types
 * 
 * @enum {number}
 * @readonly
 * @property {number} LIGHT - the armor has light weight
 * @property {number} MEDIUM - the armor has medium weight
 * @property {number} HEAVY - the armor has heavy weight
*/

enum armorType {
    LIGHT,
    MEDIUM,
    HEAVY
}

export default armorType;