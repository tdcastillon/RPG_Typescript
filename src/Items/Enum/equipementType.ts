/**
 * Enum that represents the type of an equipement
 * 
 * @enum {number}
 * @readonly
 * @property {number} WEAPON - the equipement is a weapon
 * @property {number} ARMOR - the equipement is an armor
 * @property {number} ACCESSORY - the equipement is an accessory
*/
enum EquipementType {
    WEAPON,
    ARMOR,
    ACCESSORY
}

export default EquipementType;