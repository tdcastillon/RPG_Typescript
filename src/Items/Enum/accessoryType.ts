/**
 * Enum for accessory types
 * 
 * @enum {number}
 * @readonly
 * @property {number} SHIELD - the accessory is a shield
 * @property {number} HELMET - the accessory is a helmet
 * @property {number} AMULET - the accessory is an amulet
 * @property {number} RING - the accessory is a ring
*/

enum accessoryType {
    SHIELD,
    HELMET,
    AMULET,
    RING
}

export default accessoryType;